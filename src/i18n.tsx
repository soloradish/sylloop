type TranslationParams = Record<string, string | number>;

function interpolate(message: string, params?: TranslationParams): string {
  if (!params) return message;
  return message.replace(/\{(\w+)\}/g, (match, key: string) => String(params[key] ?? match));
}

export interface I18nValue {
  locale: AppLocale;
  t: (key: MessageKey, params?: TranslationParams) => string;
  formatNumber: (value: number, options?: Intl.NumberFormatOptions) => string;
  formatSeconds: (value: number) => string;
  formatMinutes: (value: number) => string;
  formatFileCount: (value: number) => string;
  formatSegmentCount: (value: number) => string;
}

function createI18nValue(locale: AppLocale): I18nValue {
  const catalog = catalogs[locale] ?? en;
  const numberFormatter = new Intl.NumberFormat(locale, { maximumFractionDigits: 2 });
  const pluralRules = new Intl.PluralRules(locale);
  const t = (key: MessageKey, params?: TranslationParams) => interpolate(catalog[key] ?? en[key], params);
  const formatNumber = (number: number, options?: Intl.NumberFormatOptions) => (
    options ? new Intl.NumberFormat(locale, options).format(number) : numberFormatter.format(number)
  );
  const pluralized = (number: number, one: MessageKey, other: MessageKey, param: "value" | "count") => (
    t(pluralRules.select(number) === "one" ? one : other, { [param]: formatNumber(number) })
  );
  return {
    locale,
    t,
    formatNumber,
    formatSeconds: (number) => pluralized(number, "unit.second.one", "unit.second.other", "value"),
    formatMinutes: (number) => pluralized(number, "unit.minute.one", "unit.minute.other", "value"),
    formatFileCount: (number) => pluralized(number, "playlist.fileCount.one", "playlist.fileCount.other", "count"),
    formatSegmentCount: (number) => pluralized(number, "timeline.segmentCount.one", "timeline.segmentCount.other", "count"),
  };
}

const I18nContext = createContext<I18nValue>(createI18nValue("zh-CN"));

export function I18nProvider({ locale, children }: { locale: AppLocale; children: ReactNode }) {
  useEffect(() => {
    document.documentElement.lang = locale;
  }, [locale]);

  const value = useMemo<I18nValue>(() => createI18nValue(locale), [locale]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n(): I18nValue {
  return useContext(I18nContext);
}
