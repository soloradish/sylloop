export const APP_LOCALES: readonly AppLocale[] = ["zh-CN", "zh-Hant", "en", "fr"];

export const LANGUAGE_OPTIONS: ReadonlyArray<{ value: AppLocale; label: string }> = [
  { value: "zh-CN", label: "简体中文" },
  { value: "zh-Hant", label: "繁體中文" },
  { value: "en", label: "English" },
  { value: "fr", label: "Français" },
];

export function isAppLocale(value: unknown): value is AppLocale {
  return typeof value === "string" && APP_LOCALES.includes(value as AppLocale);
}

export function detectLocale(languages: readonly string[]): AppLocale {
  for (const candidate of languages) {
    const normalized = candidate.trim().toLowerCase();
    if (!normalized) continue;
    if (normalized === "zh-hant" || normalized.startsWith("zh-hant-") || /^(zh-(tw|hk|mo))(?:-|$)/.test(normalized)) {
      return "zh-Hant";
    }
    if (normalized === "zh" || normalized.startsWith("zh-") || normalized === "zh-hans" || normalized.startsWith("zh-hans-")) {
      return "zh-CN";
    }
    if (normalized === "en" || normalized.startsWith("en-")) return "en";
    if (normalized === "fr" || normalized.startsWith("fr-")) return "fr";
  }
  return "en";
}

